	public String makeCodeForCN(String gd_code){
		
		//保存机构代码
		String codeNum="gd_"+gd_code;
		
		try {
			Class cla=Class.forName("poi.bean.CodeNameCollection");
			Field [] fieldlist = cla.getDeclaredFields();
			String fieldName="";
			for(int i=0;i<fieldlist.length;i++){
				fieldName=fieldlist[i].getName();
				if(codeNum.equals(fieldName)){
					
					return (String)fieldlist[i].get(fieldName);
				}
				
			}
		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}catch (IllegalArgumentException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (IllegalAccessException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
		return gd_code;
	}
	
